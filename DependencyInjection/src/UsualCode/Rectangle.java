package UsualCode;

public class Rectangle {

    private int length;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getBreath() {
        return breath;
    }

    public void setBreath(int breath) {
        this.breath = breath;
    }

    public Rectangle(int length, int breath) {
        this.length = length;
        this.breath = breath;
    }

    private int breath;

}
